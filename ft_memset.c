#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*arr;

	i = 0;
	arr = b;
	c = (unsigned char)c;
	while (i < len)
	{
		arr[i] = c;
		i++;
	}
	return (b);
}
